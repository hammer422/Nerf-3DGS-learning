def get_rays_np(
        H, W, K, c2w, dist=None
):
    assert dist is not None
    i, j = np.meshgrid(np.arange(W, dtype=np.float32), np.arange(H, dtype=np.float32), indexing='xy')
    pts = np.stack([i, j], axis=-1).reshape(-1, 1, 2)  # 形状 [N,1,2] 供 OpenCV 使用

    fx, fy = K[0,0], K[1,1]
    cx, cy = K[0,2], K[1,2]

    # 2) 去畸变并得到归一化坐标 (x_n, y_n)
    if dist is not None:
        K_cv = np.array([[fx, 0, cx],
                         [0, fy, cy],
                         [0,  0,  1]], dtype=np.float32)
        dist = np.asarray(dist, dtype=np.float32).reshape(-1, 1)  # 兼容形状
        # R=None, P=None → 输出为归一化坐标（以 z=1 平面投影）
        undist = cv2.undistortPoints(pts, K_cv, dist, R=None, P=None)  # [N,1,2]
        x_n = undist[..., 0].reshape(H, W)
        y_n = undist[..., 1].reshape(H, W)
    else:
        assert False
        # 无畸变：按针孔模型直接归一化
        x_n = (i - cx) / fx
        y_n = (j - cy) / fy

    # 3) 相机系方向：z 前、y 下
    dirs_cam = np.stack([x_n, y_n, np.ones_like(x_n)], axis=-1)  # [H, W, 3]

    # 4) 调整射线到world下
    rays_d = np.zeros_like(dirs_cam)
    for i in range(dirs_cam.shape[0]):
        for j in range(dirs_cam.shape[1]):
            rays_d[i,j,:] = c2w[:3,:3] @ dirs_cam[i,j,:]

    # # 和原始实现对比
    # rays_d2 = np.sum(dirs_cam[..., np.newaxis, :] * c2w[:3, :3],
    #                 -1)  # dot product, equals to: [c2w.dot(dir) for dir in dirs]
    #
    # assert (rays_d == rays_d2).all()

    rays_o = np.broadcast_to(c2w[:3,-1], np.shape(rays_d))
    return rays_o, rays_d


